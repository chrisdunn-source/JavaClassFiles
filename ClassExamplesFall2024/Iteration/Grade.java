public class Grade {
    public static void main(String[] args) {
        int testGrade = 95;

        if (testGrade > 70){
            System.out.println("I made a C");
        } else if (testGrade > 80) {
            System.out.println("I made a B");

        } else if (testGrade > 90) {
            System.out.println("I made an A");

        }else {
            System.out.println("I made an F");
        }

    }
}
